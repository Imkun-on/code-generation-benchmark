def _resolve_exe(name: str, fallbacks=(), prefer_fallbacks: bool = False) -> str | None:
    """Path of the executable: from the PATH (shutil.which) and from the fallbacks
    (glob, newest version). Normally the PATH takes precedence; with
    prefer_fallbacks the fallbacks are tried FIRST (needed for `bash`, where the
    PATH may resolve WSL's bash instead of Git's). None if not found."""
    def _from_fallbacks():
        for pattern in fallbacks:
            hits = sorted(glob.glob(pattern))
            if hits:
                return hits[-1]
        return None

    if prefer_fallbacks:
        return _from_fallbacks() or shutil.which(name)
    return shutil.which(name) or _from_fallbacks()


