def _ds1000_insert_indent(code_context: str) -> str | None:
    """Indentation required by the solution in DS-1000's 'Insertion' format.

    If in the harness `[insert]` is INSIDE a block — the line preceding it ends
    with ':' (typically `def f(df):`) — the solution must be the indented body of
    that block: returns the indentation to apply (the opening line's indent + 4
    spaces). Otherwise `None`: module-level insertion ('Completion' format), no
    re-indentation."""
    i = code_context.find("[insert]")
    if i == -1:
        return None
    before = code_context[:i].splitlines()
    prev = before[-1] if before else ""
    if prev.strip().endswith(":"):
        return " " * ((len(prev) - len(prev.lstrip())) + 4)
    return None


def _build_program_ds1000(problem: dict, code: str) -> str:
    """Assemble the verification program for a DS-1000 problem.

    The `code_context` is already a complete program that DEFINES
    `test_execution(solution)` (and possibly `test_string(solution)`) but does not
    call it. The model's solution must be passed as a STRING: test_execution
    inserts it in place of `[insert]` in its template and runs, raising
    AssertionError if wrong.

    So: code_context + a call to test_execution(<solution>). We pass the solution
    with repr() to get a safe Python literal (handles newlines, quotes, etc.). For
    Matplotlib problems we force the non-interactive 'Agg' backend, so it does not
    try to open windows in a headless process.

    'Insertion' format: when `[insert]` is the body of a `def`, we do NOT rely on
    the model to indent (it does so inconsistently: first line at column 0, the
    rest indented → IndentationError). We re-indent ourselves: `dedent` normalizes
    any uniform indent, `indent` applies the required one while preserving the
    relative structure (even with nested loops). The prompt indeed asks for the
    body at column 0."""
    cc = problem["code_context"]
    header = ""
    if problem.get("metadata", {}).get("library") == "Matplotlib":
        header = "import matplotlib\nmatplotlib.use('Agg')\n\n"

    indent = _ds1000_insert_indent(cc)
    if indent is not None and code.strip():
        code = textwrap.indent(textwrap.dedent(code), indent)

    parts = [header + cc, "", f"solution = {code!r}", "test_execution(solution)"]
    if "def test_string" in cc:
        parts.append("test_string(solution)")
    return "\n".join(parts)


