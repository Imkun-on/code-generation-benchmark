"""
executor.py — Runs the generated code against the unit tests and classifies the
outcome (pass@1) and, on failure, the error CATEGORY.

The per-category classification is the central data point for analyzing "where"
the different architectures go wrong.

⚠️  SECURITY: the code generated by the models is untrusted code. We run it in a
separate subprocess with a timeout. Only launch the benchmark on a
machine/environment you can sacrifice (or in a container).
"""

