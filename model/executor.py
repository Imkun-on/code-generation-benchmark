def _kill_tree(pid: int) -> None:
    """Kill the process `pid` AND ALL its descendants.

    Necessary because `proc.kill()` on Windows (TerminateProcess) terminates ONLY
    the direct process, not its children. When the generated code spawns a
    subprocess (e.g. bash opening a `| bc` pipe, a command-substitution in a loop,
    a `&` job), that grandchild INHERITS the stdout/stderr pipes and stays alive:
    the `communicate()` that follows the kill would wait forever for EOF on a
    still-open pipe → the whole benchmark hangs forever on that problem."""
    if os.name == "nt":
        subprocess.run(["taskkill", "/F", "/T", "/PID", str(pid)],
                       capture_output=True)
    else:
        import signal as _signal
        try:
            os.killpg(os.getpgid(pid), _signal.SIGKILL)
        except (ProcessLookupError, PermissionError):
            pass


def _run_guarded(cmd, timeout=None, **kwargs):
    """Like `subprocess.run(..., timeout=...)`, but on timeout it kills the WHOLE
    process tree instead of just the direct child (see _kill_tree). Raises
    `subprocess.TimeoutExpired` exactly like `subprocess.run`, so the existing
    call sites that catch it don't change. Identical behavior to `subprocess.run`
    when there is no timeout."""
    capture = kwargs.pop("capture_output", False)
    if capture:
        kwargs.setdefault("stdout", subprocess.PIPE)
        kwargs.setdefault("stderr", subprocess.PIPE)
    if os.name != "nt":
        kwargs.setdefault("start_new_session", True)  # gruppo proprio → killpg
    with subprocess.Popen(cmd, **kwargs) as proc:
        try:
            stdout, stderr = proc.communicate(timeout=timeout)
        except subprocess.TimeoutExpired:
            _kill_tree(proc.pid)
            try:
                # Ora i discendenti sono morti: le pipe si chiudono, communicate ritorna.
                stdout, stderr = proc.communicate(timeout=5)
            except subprocess.TimeoutExpired:
                proc.kill()
                stdout, stderr = proc.communicate()
            raise subprocess.TimeoutExpired(cmd, timeout, output=stdout, stderr=stderr)
        except BaseException:
            _kill_tree(proc.pid)
            raise
    return subprocess.CompletedProcess(cmd, proc.returncode, stdout, stderr)


