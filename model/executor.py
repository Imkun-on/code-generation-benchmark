def _imports_header(prompt: str) -> str:
    """Extract the leading import lines from the HumanEval prompt.

    They are needed because the test (and sometimes the solution) use types like
    List/Dict imported at the top of the original prompt.

    ⚠️ We take ONLY the module-level imports (column 0, NOT indented). If we used
    line.strip(), a wrapped docstring line starting with "from"/"import" would be
    mistaken for an import and injected into the program. This is exactly what
    happened on HumanEval/99, whose docstring contains "...equidistant\n    from
    two integers, round it away from zero.": that line ended up at the top of the
    file → IndentationError → a CORRECT solution failed.
    """
    header = []
    for line in prompt.splitlines():
        if line.startswith(("import ", "from ")):  # niente strip: solo colonna 0
            header.append(line)
    return "\n".join(header)


def _build_program(problem: dict, code: str) -> str:
    """Assemble the HumanEval verification program: imports + generated function
    + test + the `check(entry_point)` call."""
    header = _imports_header(problem["prompt"])
    entry = problem["entry_point"]
    return (
        f"{header}\n\n"
        f"{code}\n\n"
        f"{problem['test']}\n\n"
        f"check({entry})\n"
    )


def _build_program_mbpp(problem: dict, code: str) -> str:
    """Assemble the verification program for an MBPP problem.

    MBPP is structured differently from HumanEval:
      - no `prompt` with imports and no `entry_point`/`check` function;
      - `test_setup_code` (may be empty) goes first: it sometimes defines imports
        or support structures required by the asserts;
      - `test_list` is a LIST of self-contained `assert`s: we append them, one per
        line. If one fails it raises AssertionError, exactly like for HumanEval →
        same classification in _classify_stderr.

    Note: we use ONLY `code` (the model's code) + the tests. The CodeBLEU
    reference remains the dataset's original `code` field, computed elsewhere
    (metrics.py): it is irrelevant here and the tests NEVER end up inside it.
    """
    setup = problem.get("test_setup_code", "") or ""
    asserts = "\n".join(problem.get("test_list", []))
    return (
        f"{setup}\n\n"
        f"{code}\n\n"
        f"{asserts}\n"
    )


