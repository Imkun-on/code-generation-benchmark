"""
plot2code.py — Loading the Plot2Code benchmark (TencentARC/Plot2Code).

Plot2Code (Wu et al., 2024) evaluates the generation of code that reproduces a
scientific chart. Each example is a plot from the matplotlib gallery with:
  url          link to the source gallery page
  code         reference matplotlib code (gold)
  instruction  detailed textual description of the figure (generated by GPT-4)
  image        the rendered reference plot (PIL Image)

USAGE IN OUR PROJECT (design choice)
--------------------------------------------
Input to the model = `instruction` (TEXT ONLY, NOT multimodal): this way even
the text-only models of the by-architecture comparison (DeepSeek MoE, Ministral
SLM) can take part. The model generates the matplotlib script; we evaluate it with:
  1. CodeBLEU      — generated code vs `code` (metrics.py)
  2. functional correctness — the code RUNS and produces a figure (executor.py)
  3. image similarity — render of the generated code vs the reference `image`
                        (metric to be defined; for now we only save the images)

Unlike the other benchmarks there are NO unit tests: "correctness" is the fact
that the code executes and produces a non-empty PNG (the paper's "code pass rate").

DATA ON DISK
-------------
Like the other benchmarks, on the first run the dataset is saved to
Benchmark/plot2code/ (save_to_disk, short names for Windows' 260-char limit). The
reference images are extracted once to Benchmark/plot2code/ref_images/<id>.png and
in the records we pass the PATH (PIL Images are not serializable in the JSON
checkpoint). On Windows the HF download requires HF_HUB_DISABLE_SYMLINKS=1 (see the
env-gotchas memo), which we set here.
"""

import os
from pathlib import Path

BENCHMARK_DIR = Path(__file__).resolve().parent.parent / "Benchmark"
PLOT2CODE_DIR = BENCHMARK_DIR / "plot2code"
DATASET_DIR = PLOT2CODE_DIR / "dataset"           # arrow (save_to_disk)
REF_IMAGES_DIR = PLOT2CODE_DIR / "ref_images"     # <task_id>.png di riferimento


def _ensure_ref_image(image, task_id: int) -> str:
    """Save (once) the PIL reference image to disk and return its absolute path.
    The images are needed for the visual comparison and CANNOT live in the record
    (not serializable to JSON)."""
    REF_IMAGES_DIR.mkdir(parents=True, exist_ok=True)
    path = REF_IMAGES_DIR / f"{task_id}.png"
    if not path.exists():
        image.save(path)
    return str(path)


def load_plot2code(limit: int | None = None) -> list[dict]:
    """
    Load the Plot2Code problems.

    First run: downloads TencentARC/Plot2Code from Hugging Face, saves it to
    Benchmark/plot2code/dataset and extracts the reference images.
    Subsequent runs: reads from disk (no download).

    limit: if set, takes only the first N examples (quick/cheap runs).

    Each record:
      task_id      stable integer index (checkpoint/dedup/report)
      instruction  description of the figure (input to the model)
      code         reference matplotlib code (CodeBLEU reference)
      url          source page
      ref_image    path to the reference PNG (for the visual comparison)
    """
    BENCHMARK_DIR.mkdir(exist_ok=True)
    # Windows: evita il fallimento symlink della cache HF (WinError 1314).
    os.environ.setdefault("HF_HUB_DISABLE_SYMLINKS", "1")
    os.environ.setdefault("HF_HUB_DISABLE_SYMLINKS_WARNING", "1")

    from datasets import load_from_disk

    if DATASET_DIR.exists():
        ds = load_from_disk(str(DATASET_DIR))
    else:
        from datasets import load_dataset
        loaded = load_dataset("TencentARC/Plot2Code")
        ds = loaded[list(loaded.keys())[0]]        # unico split: 'test'
        DATASET_DIR.parent.mkdir(parents=True, exist_ok=True)
        ds.save_to_disk(str(DATASET_DIR))

    n = len(ds) if limit is None else min(limit, len(ds))
    records: list[dict] = []
    for i in range(n):
        row = ds[i]
        records.append({
            "task_id": i,
            "instruction": row.get("instruction", "") or "",
            "code": row.get("code", "") or "",
            "url": row.get("url", "") or "",
            "ref_image": _ensure_ref_image(row["image"], i),
        })
    return records
